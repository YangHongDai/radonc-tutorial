def polish_zh_output(text):
    s = text
    for rx, sub in POST_REPLACEMENTS:
        s = rx.sub(sub, s)
    s = re.sub(r"\s+([,.;:!?%])", r"\1", s)
    s = re.sub(r"([（(])\s+", r"\1", s)
    s = re.sub(r"\s+([）)])", r"\1", s)
    s = re.sub(r"\s{2,}", " ", s)
    return s.strip()

def translate_en_to_zh(text):
    """Apply term substitutions to render an English medical string in mixed ZH form."""
    if not text: return text
    s = text
    for rx, sub in COMPILED:
        s = rx.sub(sub, s)
    # Tidy spacing between CJK and Latin (basic pass)
    s = re.sub(r'([一-鿿])\s+([A-Za-z0-9])', r'\1 \2', s)
    s = re.sub(r'([A-Za-z0-9])\s+([一-鿿])', r'\1 \2', s)
    s = re.sub(r'\s{2,}', ' ', s)
    return polish_zh_output(s)

