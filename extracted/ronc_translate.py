# Pre-compile regexes (word-boundary if pattern starts/ends with letters).
def compile_pair(pat, sub):
    # Always wrap with word boundaries when both ends are word-characters.
    starts_word = re.match(r'^\w', pat) is not None
    ends_word = re.search(r'\w$', pat) is not None
    esc = re.escape(pat)
    if starts_word and ends_word:
        return (re.compile(r'\b' + esc + r'\b', re.IGNORECASE), sub)
    return (re.compile(esc, re.IGNORECASE), sub)

